
#include<stdio.h>

void StrtoggalX(char str[])          //S a R v E s H         
{

    while(*str != '\0')
    {
        if (*str >= 'a' && *str <= 'z')          
        {
            *str = (*str - 32);
        }
        else if (*str >= 'A' && *str <= 'Z')
        {
            *str = (*str + 32);
        }
        
        str++;
    }

}

int main()
{
    char Arr[50] = {'\0'};

    printf("Enter string :- \n");
    scanf("%[^'\n']s",Arr); 

    StrtoggalX(Arr);

    printf("Updated string is : %s\n",Arr);

    return 0;
}
